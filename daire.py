# 4) Dairenin alanını ve çevreesini hesaplayan kodu yazınız. 
# dairenin çevre formülü:2*pi*daireninyarıçapı
# dairenin alan formülü:pi*yarıçapınkaresi

yaricap = float (input( "Lütfen dairenin yarıçapını giriniz:"))
cevre= 2 * 3.14 * yaricap
alan = 3.14 * yaricap**2
print ("Dairenin Çevresi" , cevre)
print ("Dairenin Alanı:", alan)
print ("Pi değeri 3.14 alınmıştır")